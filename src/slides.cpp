// ------------------------------------------------------------
// ГЛОБАЛЬНЫЕ ФЛАГИ (volatile – изменяются в прерывании)
// ------------------------------------------------------------
volatile bool requestUpdate = false;      // запрос на обновление дисплея
volatile bool showSecond = false;         // какой битмап показывать (true – второй)

// ------------------------------------------------------------
// НАСТРОЙКА ПРЕРЫВАНИЯ INT0 (пин PD2)
// ------------------------------------------------------------
void initInterrupt() {
  // Пин PD2 как вход с подтяжкой (кнопка замыкает на GND)
  DDRD  &= ~(1 << PD2);    // вход
  PORTD |=  (1 << PD2);    // подтяжка к VCC

  cli();                     // запретить прерывания на время настройки
  EICRA |= (1 << ISC01);     // прерывание по спаду (FALLING) – при нажатии кнопки
  EIMSK |= (1 << INT0);      // разрешить INT0
  sei();                     // глобально разрешить прерывания
}

// ------------------------------------------------------------
// ОБРАБОТЧИК ПРЕРЫВАНИЯ – МИНИМАЛЬНЫЙ (только флаги)
// ------------------------------------------------------------
ISR(INT0_vect) {
  requestUpdate = true;       // сообщаем основному циклу, что нужно обновить экран
}

// ------------------------------------------------------------
// ФУНКЦИЯ ОБНОВЛЕНИЯ ДИСПЛЕЯ (рисует выбранный битмап)
// ------------------------------------------------------------
void updateDisplay() {
  u8g2.firstPage();
  do {
    if (showSecond) {
      u8g2.drawXBMP(0, 0, 128, 64, bitmap_2);
    } else {
      u8g2.drawXBMP(0, 0, 128, 64, bitmap_1);
    }
  } while (u8g2.nextPage());
}

// ------------------------------------------------------------
// СТАНДАРТНЫЕ ФУНКЦИИ ARDUINO
// ------------------------------------------------------------
void setup() {
  // Инициализация дисплея
  u8g2.begin();

  // Настройка светодиода на PD5
  DDRD  |= (1 << PD5);       // выход
  PORTD &= ~(1 << PD5);      // начальное состояние – выключен

  // Настройка прерывания
  initInterrupt();

  // Первое отображение (битмап 1)
  updateDisplay();
}

void loop() {
  // Если есть запрос на обновление от прерывания
  if (requestUpdate) {
    requestUpdate = false;          // сброс флага

    // Переключаем режим (меняем картинку)
    showSecond = !showSecond;

    // Короткая индикация светодиодом
    PORTD |= (1 << PD5);
    delay(50);
    PORTD &= ~(1 << PD5);

    // Обновляем дисплей
    updateDisplay();
  }

  // Здесь можно добавить другие задачи, не связанные с дисплеем
  // Например, чтение датчиков, обработка других кнопок и т.д.
}